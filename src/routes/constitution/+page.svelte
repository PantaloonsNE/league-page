<h2>11. Rule Changes &amp; Voting</h2>

<h3>Minor changes require majority (7 of 12)</h3>
<ul>
  <li>Tweaks that don’t fundamentally change player values or long-term teambuilding strategies.</li>
  <li>Side bet formats</li>
  <li>Changing waiver processing days/times</li>
</ul>

<h3>Major changes (scoring, format) require supermajority (9 of 12)</h3>
<ul>
  <li>Shifts that alter player values, draft capital, or long-term roster building.</li>
  <li>Scoring System changes</li>
  <li>Lineup Requirements</li>
</ul>

<h2>12. Commissioners</h2>
<p>NSSDL is run by two co-commissioners</p>
<p>Responsibilities include:</p>
<ul>
  <li>Overseeing waivers, trades, disputes</li>
  <li>Enforcing rules and constitution</li>
  <li>Maintaining league integrity and engagement</li>
</ul>
<p>If needed, a third-party vote (league-wide) can be called for conflict resolution</p>
