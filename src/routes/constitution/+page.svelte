<h1>NSSDL Dynasty League Constitution</h1>

<h2>Preamble</h2>
<p>We, the noble degenerates of the Nebraska State-Sponsored Dynasty League, in order to form a more perfect fantasy union, establish fair rules, ensure maximum trash talk, provide for eternal rivalries, promote late-night trade negotiations, and secure bragging rights for ourselves and our posterity, do hereby establish this sacred Constitution. May it forever guide us through bye weeks, injuries, and the cruel wrath of fantasy variance.</p>

<h2>1. League Overview</h2>
<ul>
  <li>Name: NSSDL (Nebraska State-Sponsored Dynasty League)</li>
  <li>Teams: 12</li>
  <li>Format: Dynasty (players kept year-to-year)</li>
  <li>Scoring: Half-PPR (0.5 points per reception)</li>
  <li>Platform: Sleeper</li>
  <li>Buy-in: $100 annually per team</li>
  <li>Total Prize Pool: $1,200</li>
  <li>Payouts &amp; Side Bets: See Section 10</li>
</ul>

<h2>2. Rosters</h2>
<ul>
  <li>Total Roster Size: 26 players</li>
  <li>IR Spots: 2</li>
  <li>Starting Lineup:
    <ul>
      <li>1 QB</li>
      <li>2 RB</li>
      <li>2 WR</li>
      <li>1 TE</li>
      <li>1 FLEX (RB/WR/TE)</li>
      <li>1 FLEX (WR/TE)</li>
      <li>1 K</li>
      <li>2 IDP (Individual Defensive Players)</li>
      <li>15 Bench Spots</li>
    </ul>
  </li>
</ul>

<h2>3. Scoring Settings</h2>

<h3>Offensive Scoring:</h3>
<ul>
  <li>Passing:
    <ul>
      <li>Passing yards: 1 pt per 25 yards</li>
      <li>Passing TD: 4 pts</li>
      <li>2-pt conversions: 2 pts</li>
      <li>Interceptions: -2 pts</li>
    </ul>
  </li>
  <li>Rushing:
    <ul>
      <li>Rushing yards: 1 pt per 10 yards</li>
      <li>Rushing TD: 6 pts</li>
      <li>Fumble lost: -2 pts</li>
      <li>2-pt conversions: 2 pts</li>
      <li>Fumble Recovery TD: 6 pts</li>
    </ul>
  </li>
  <li>Receiving:
    <ul>
      <li>Receiving yards: 1 pt per 10 yards</li>
      <li>Half-PPR: 0.5 points per reception</li>
      <li>Receiving TD: 6 pts</li>
      <li>2-pt Conversions: 2 pts</li>
    </ul>
  </li>
  <li>Kicking:
    <ul>
      <li>FG Made 0-39 yards: 3 pts</li>
      <li>FG Made 40-49 yards: 4 pts</li>
      <li>FG Made 50-59 yards: 5 pts</li>
      <li>FG Made 60+ yards: 6 pts</li>
      <li>PAT (Point After Touchdown): 1 pt</li>
      <li>Missed FG: -1 pt</li>
    </ul>
  </li>
  <li>Return Yards:
    <ul>
      <li>Punt Return yards: 1 pt per 20 yards</li>
      <li>Kick Return yards: 1 pt per 20 yards</li>
      <li>Return TD: 6 pts</li>
    </ul>
  </li>
</ul>

<h3>IDP (Individual Defensive Player) Scoring:</h3>
<ul>
  <li>IDP TD: 6 pts</li>
  <li>Sack: 4 pts</li>
  <li>TFL (Tackles for Loss): 2 pts</li>
  <li>Block Punt, FG, PAT: 2 pts</li>
  <li>Interception (INT): 5 pts</li>
  <li>Forced Fumble (FF): 4 pts</li>
  <li>Fumble Recovery: 4 pts</li>
  <li>Safety: 2 pts</li>
  <li>Pass Defended: 3 pts</li>
  <li>Assisted Tackle: 0.75 pts</li>
  <li>Solo Tackle: 1.5 pts</li>
</ul>

<h3>Bonus Points:</h3>
<ul>
  <li>100-199 Yard Rushing Game: 1 pt</li>
  <li>200+ Yard Rushing Game: 2 pts</li>
  <li>100-199 Yard Receiving Game: 1 pt</li>
  <li>200+ Yard Receiving Game: 2 pts</li>
  <li>300-399 Yard Passing Game: 1 pt</li>
  <li>400+ Yard Passing Game: 2 pts</li>
</ul>

<h2>4. Drafts</h2>

<h3>4.1 Startup Draft</h3>
<ul>
  <li>One-time draft to initiate the league, snake format.</li>
  <li>12 hour clock with a pause 9pm - 7am</li>
</ul>

<h3>4.2 Rookie Draft</h3>
<ul>
  <li>Held each offseason.</li>
  <li>4 rounds total in traditional order.</li>
  <li>Non-playoff teams: Draft order determined by a weighted lottery system based on Max Points For (PF).</li>
  <li>Playoff teams: Draft order determined by reverse order of playoff finish.</li>
</ul>

<h2>5. Player Transactions</h2>
<ul>
  <li>Waivers: FAAB system</li>
  <li>Budget: $200 per team, per season</li>
  <li>Free Agents: First-come, first-served after weekly waiver processing</li>
  <li>Trades:
    <ul>
      <li>Allowed up to the end of Week 14 (final week of regular season)</li>
      <li>Future draft picks may be traded up to 3 years in advance</li>
      <li>Trades processed immediately unless flagged for collusion</li>
      <li>Commissioners reserve the right to pause and investigate any trade suspected of collusion or obvious imbalance.</li>
      <li>Co-commissioners review disputes or questionable trades</li>
    </ul>
  </li>
</ul>

<h2>6. Schedule &amp; League Median Matchups</h2>
<ul>
  <li>Each team plays:</li>
  <li>One head-to-head matchup per week</li>
  <li>One additional game each week against the league median score</li>
  <li>The league median is the 6th-highest score of the week (in a 12-team league)</li>
  <li>If your team scores above the median, you earn a win</li>
  <li>If your team scores below the median, you receive a loss</li>
  <li>Each team will have 28 regular-season results (14 head-to-head + 14 median games)</li>
  <li>These results will count toward playoff qualification and seeding</li>
</ul>

<h2>7. League Integrity</h2>
<ul>
  <li>Lineups must always be legal and competitive.</li>
  <li>No tanking allowed; Draft order settings should eliminate this.</li>
  <li>All rule infractions will be handled by the co-commissioners.</li>
</ul>

<h3>7.1 Collusion &amp; Prohibited Conduct</h3>
<p>To preserve fairness and competition, the following behaviors are strictly prohibited:</p>

<h4>Borrowing &amp; Asset Leverage</h4>
<ul>
  <li>Managers may not “borrow” players, picks, or FAAB with the promise of future compensation outside of formal trade agreements.</li>
  <li>No temporary “rentals” are allowed (e.g., trading for a player during a playoff push or injury/bye week stretch with an understanding they’ll be traded back).</li>
  <li>Example: It is not permitted to acquire a player to cover bye weeks or injuries with a prearranged plan to return that player in a few weeks.</li>
  <li>Every trade must stand on its own merit and be enforceable as final at the time of execution.</li>
</ul>

<h4>Real-Money Incentives</h4>
<ul>
  <li>Managers may not offer or accept real money, services, or goods in exchange for fantasy football outcomes (e.g., wins, trades, players, picks).</li>
  <li>All competitive and transactional activity must occur within the bounds of league play and prize structure as defined in this constitution.</li>
</ul>

<h4>Enforcement</h4>
<ul>
  <li>Allegations of collusion or prohibited conduct will be reviewed by the co-commissioners.</li>
  <li>Penalties may include reversal of trades, loss of draft picks, forfeiture of games, or removal from the league depending on severity.</li>
  <li>If a commissioner is directly involved or there is a conflict of interest, a league-wide vote may be called to determine appropriate disciplinary action.</li>
</ul>

<h2>8. Orphan Teams</h2>
<ul>
  <li>If a manager departs, the co-commissioners will:</li>
  <li>Recruit a new manager</li>
  <li>May offer incentives (discounted entry, extra picks) for balance if needed.</li>
</ul>

<h2>9. Playoffs</h2>
<ul>
  <li>6-team bracket</li>
  <li>Weeks 15–17</li>
  <li>Top 2 seeds get first-round byes</li>
  <li>Tiebreakers: Record &gt; PF &gt; H2H &gt; All-play record</li>
</ul>

