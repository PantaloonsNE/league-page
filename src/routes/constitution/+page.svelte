  <h1>NSSDL Dynasty League Constitution</h1>

  <h2>Preamble</h2>
  <p>
    We, the noble degenerates of the Nebraska State-Sponsored Dynasty League, in order to form a more perfect fantasy union, establish fair rules, ensure maximum trash talk, provide for eternal rivalries, promote late-night trade negotiations, and secure bragging rights for ourselves and our posterity, do hereby establish this sacred Constitution. May it forever guide us through bye weeks, injuries, and the cruel wrath of fantasy variance.
  </p>

  <h2>1. League Overview</h2>
  <ul>
    <li><strong>Name:</strong> NSSDL (Nebraska State-Sponsored Dynasty League)</li>
    <li><strong>Teams:</strong> 12</li>
    <li><strong>Format:</strong> Dynasty (players kept year-to-year)</li>
    <li><strong>Scoring:</strong> Half-PPR (0.5 points per reception)</li>
    <li><strong>Platform:</strong> Sleeper</li>
    <li><strong>Buy-in:</strong> $100 annually per team</li>
    <li><strong>Total Prize Pool:</strong> $1,200</li>
    <li><strong>Payouts & Side Bets:</strong> See Section 10</li>
  </ul>

  <h2>2. Rosters</h2>
  <ul>
    <li>Total Roster Size: 26 players</li>
    <li>IR Spots: 2</li>
    <li><strong>Starting Lineup:</strong>
      <ul>
        <li>1 QB</li>
        <li>2 RB</li>
        <li>2 WR</li>
        <li>1 TE</li>
        <li>1 FLEX (RB/WR/TE)</li>
        <li>1 FLEX (WR/TE)</li>
        <li>1 K</li>
        <li>2 IDP (Individual Defensive Players)</li>
        <li>15 Bench Spots</li>
      </ul>
    </li>
  </ul>

  <h2>3. Scoring Settings</h2>
  <h3>Offensive Scoring</h3>
  <ul>
    <li><strong>Passing:</strong>
      <ul>
        <li>Passing yards: 1 pt per 25 yards</li>
        <li>Passing TD: 4 pts</li>
        <li>2-pt conversions: 2 pts</li>
        <li>Interceptions: -2 pts</li>
      </ul>
    </li>
    <li><strong>Rushing:</strong>
      <ul>
        <li>Rushing yards: 1 pt per 10 yards</li>
        <li>Rushing TD: 6 pts</li>
        <li>Fumble lost: -2 pts</li>
        <li>2-pt conversions: 2 pts</li>
        <li>Fumble Recovery TD: 6 pts</li>
      </ul>
    </li>
    <li><strong>Receiving:</strong>
      <ul>
        <li>Receiving yards: 1 pt per 10 yards</li>
        <li>Half-PPR: 0.5 points per reception</li>
        <li>Receiving TD: 6 pts</li>
        <li>2-pt Conversions: 2 pts</li>
      </ul>
    </li>
    <li><strong>Kicking:</strong>
      <ul>
        <li>FG Made 0-39 yards: 3 pts</li>
        <li>FG Made 40-49 yards: 4 pts</li>
        <li>FG Made 50-59 yards: 5 pts</li>
        <li>FG Made 60+ yards: 6 pts</li>
        <li>PAT: 1 pt</li>
        <li>Missed FG: -1 pt</li>
      </ul>
    </li>
    <li><strong>Return Yards:</strong>
      <ul>
        <li>Punt Return yards: 1 pt per 20 yards</li>
        <li>Kick Return yards: 1 pt per 20 yards</li>
        <li>Return TD: 6 pts</li>
      </ul>
    </li>
  </ul>

  <h3>IDP (Individual Defensive Player) Scoring</h3>
  <ul>
    <li>IDP TD: 6 pts</li>
    <li>Sack: 4 pts</li>
    <li>TFL: 2 pts</li>
    <li>Block Punt/FG/PAT: 2 pts</li>
    <li>Interception: 5 pts</li>
    <li>Forced Fumble: 4 pts</li>
    <li>Fumble Recovery: 4 pts</li>
    <li>Safety: 2 pts</li>
    <li>Pass Defended: 3 pts</li>
    <li>Assisted Tackle: 0.75 pts</li>
    <li>Solo Tackle: 1.5 pts</li>
  </ul>

  <h3>Bonus Points</h3>
  <ul>
    <li>100-199 Yard Rushing Game: 1 pt</li>
    <li>200+ Yard Rushing Game: 2 pts</li>
    <li>100-199 Yard Receiving Game: 1 pt</li>
    <li>200+ Yard Receiving Game: 2 pts</li>
    <li>300-399 Yard Passing Game: 1 pt</li>
    <li>400+ Yard Passing Game: 2 pts</li>
  </ul>

  <h2>4. Draft Rules</h2>
  <ul>
    <li>Startup Draft: Snake format, random order determined by lottery.</li>
    <li>Rookie Draft: 5 rounds each offseason, linear order based on draft lottery (see Section 8).</li>
    <li>Time limit per pick: 12 hours (slow draft format).</li>
    <li>Future picks can be traded up to 3 years ahead.</li>
  </ul>

  <h2>5. Free Agency & Waivers</h2>
  <ul>
    <li>Waivers via FAAB (Free Agent Acquisition Budget).</li>
    <li>Each team has $200 FAAB per season.</li>
    <li>Minimum bid: $0.</li>
    <li>FAAB does not carry over year-to-year.</li>
  </ul>

  <h2>6. Trades</h2>
  <ul>
    <li>Trades can include players, draft picks (up to 3 years ahead), and FAAB.</li>
    <li>Trade deadline: End of regular season (Week 14).</li>
    <li>No trade vetoes unless collusion is suspected; commissioners have final say.</li>
  </ul>

  <h2>7. Schedule & Playoffs</h2>
  <ul>
    <li>Regular Season: Weeks 1–14.</li>
    <li>Playoffs: Weeks 15–17.</li>
    <li>6 teams make playoffs (top 2 seeds get a bye).</li>
    <li>Seeding based on record, then points for.</li>
    <li>Consolation Bracket: Weeks 15–17 for non-playoff teams.</li>
  </ul>

  <h2>8. Draft Lottery & Order</h2>
  <ul>
    <li>Non-playoff teams enter the draft lottery.</li>
    <li>Odds determined by Max PF (Potential Points) — worst roster gets the best odds.</li>
    <li>Playoff teams draft based on finish (champion picks 12th, runner-up 11th, etc.).</li>
  </ul>

  <h2>9. Commissioners</h2>
  <ul>
    <li>2 Co-Commissioners oversee the league.</li>
    <li>Responsibilities: Rule enforcement, dispute resolution, and smooth league operations.</li>
    <li>Any changes to this Constitution require majority league approval.</li>
  </ul>

  <h2>10. Payouts & Side Bets</h2>
  <h3>Season Payouts</h3>
  <ul>
    <li>Champion: $600</li>
    <li>Runner-up: $300</li>
    <li>3rd Place: $100</li>
    <li>Regular Season Points Leader: $200</li>
  </ul>

  <h3>Weekly Side Bets</h3>
  <ul>
    <li>Highest Scoring Team Each Week: $10</li>
    <li>Lowest Scoring Team Each Week: -$10 (paid into side bet pool)</li>
    <li>Other fun challenges may be added at commissioner discretion.</li>
  </ul>

  <h2>11. Rule Changes</h2>
  <ul>
    <li>Major rule changes (scoring, roster size, payouts) require a 2/3 league majority.</li>
    <li>Minor changes (typo corrections, clarifications) can be made by commissioners.</li>
    <li>Rule change proposals must be submitted in the offseason.</li>
  </ul>

  <h2>12. Spirit of the League</h2>
  <p>
    This league is built on competition, camaraderie, and good-natured trash talk. Rivalries are encouraged, collusion is forbidden, and participation is required. Fantasy football is unpredictable, but we are here for the chaos. May the best degenerate win.
  </p>

